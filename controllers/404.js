exports.get404=(req,res,next)=>{
    res.status(404).render('404'
    ,{
        docTitle:'404',
        isAuthentcated:req.session.isLoggedIn
    })
}
exports.get500=(req,res,next)=>{
    res.status(404).render('500'
    ,{
        docTitle:'Error!',
        isAuthentcated:req.session.isLoggedIn
    })
}
